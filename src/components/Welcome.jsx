import { useRef } from "react"
import gsap from "gsap"
import { useGSAP } from "@gsap/react"
const FONT_WEIGHT= {
    subtitle: {min:100,max:400,default:100},
    title: {min:400,max:900,default:400}
}
const rendeText = (text,className,baseWeight=400)=>{
    return [...text].map((char,i)=>(
        <span key={i}
        className={className}
        style={{ fontVariationSettings: `'wght' ${baseWeight}`}}
        >
            {char === " " ? ("\u00a0") : char}

        </span>
    ))
}

const setupTextAnimation = (container ,type)=>{
    if(!container) return () => {};

    const letters = container.querySelectorAll("span");
    const {min,max,default:base}= FONT_WEIGHT[type];
    const animateLetters =(letter , weight,duration=0.25)=>{
       return gsap.to(letter,{duration,ease:'power2.out',fontVariationSettings:`'wght' ${weight}`}) 
    }

    const handleMouseMove = (e)=>{
        const {left}=container.getBoundingClientRect();
        const mouseX =e.clientX-left

        letters.forEach((letter,index)=>{
            const {left:l,width:w}=letter.getBoundingClientRect();
            const distance = Math.abs(mouseX-(l -left +w/2));
            const intensity = Math.exp(-(distance **2)/2000)
            animateLetters(letter,min +(max -min)*intensity);
        })
    }
    const handleMouseLeave =()=>{
        letters.forEach((letter)=>{
            animateLetters(letter,base,0.3);
        })
    }

    container.addEventListener("mousemove",handleMouseMove)
    container.addEventListener("mouseleave",handleMouseLeave)

    return ()=>{
        container.removeEventListener("mousemove",handleMouseMove)
        container.removeEventListener("mouseleave",handleMouseLeave)
    }
}

const Welcome = () => {
    const titleRef =useRef(null)
    const subtitleRef =useRef(null)
    useGSAP( ()=>{
        const titleCleanup=setupTextAnimation(titleRef.current,"title")
        const subtitleCleanup=setupTextAnimation(subtitleRef.current,"subtitle")
        return ()=>{
            titleCleanup()
            subtitleCleanup()
        }
    },[])

  return( <section id="welcome">
    <p ref={subtitleRef}>{rendeText("I'm Manab! Welcome to my","text-3xl font-georama",100)}</p>
    <h1 ref={titleRef} className="mt-7">{rendeText("portfolio","text-9xl italic font-georama")}</h1>

    <div className="small-screen">
        <p>This Portfolio is best viewed on a desktop.</p>
    </div>
  </section>
  )
}

export default Welcome